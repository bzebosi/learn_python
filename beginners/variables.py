# How to handle strings i.e. print

# Ask User for their name 
name = input("what's your name? ")

# say hello to user

# version 1, + means join
print("hello, " + name)

# version 2
print("hello,", name)

# version 3 includes sep = ' ' space and end="nothing"
# sep and end are name parameters
print("hello,", name, sep=' ', end="\n")

# introduction parameters

print("hello, \"friend\"")